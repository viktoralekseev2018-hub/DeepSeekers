"""
Модуль детекции микрокатегорий в тексте объявления.
Использует комбинированный подход с ключевыми фразами и контекстным анализом.
"""

import re
from typing import Set, List, Tuple
from collections import defaultdict

from .microcatalog import MICROCATALOG, MC_DICT


def normalize_text(text: str) -> str:
    """Нормализация текста: приведение к нижнему регистру, удаление лишних пробелов."""
    text = text.lower()
    text = re.sub(r'\s+', ' ', text).strip()
    return text


def extract_sentences(description: str) -> List[str]:
    """Разбивает текст на предложения/фразы."""
    # Разбиваем по точкам, запятым, точкам с запятой, слэшам
    parts = re.split(r'[.!,;/]', description)
    return [p.strip() for p in parts if p.strip()]


def calculate_phrase_score(phrase: str, normalized_text: str, mc_id: int) -> float:
    """
    Вычисляет вес фразы для данной микрокатегории.

    Args:
        phrase: Нормализованная фраза
        normalized_text: Нормализованный текст объявления
        mc_id: ID микрокатегории

    Returns:
        Вес фразы (0.0 - 1.0)
    """
    # Базовые веса для разных типов фраз
    exact_multiword_phrases = {
        101: ["ремонт под ключ", "комплексный ремонт", "полный ремонт", "комплекс работ",
              "генподряд", "ремонт квартиры под ключ", "ремонт дома под ключ"],
        102: ["сантехнические работы", "монтаж сантехники", "разводка труб", "опрессовка труб",
              "гидроизоляция под плитку", "пайка труб", "сварка труб"],
        103: ["электромонтажные работы", "замена проводки", "сборка электрощита",
              "перенос розеток", "перенос выключателя", "монтаж освещения"],
        104: ["натяжные потолки", "натяжной потолок", "монтаж натяжных потолков",
              "тканевые потолки", "двухуровневые натяжные потолки"],
        105: ["укладка плитки", "плиточные работы", "облицовка плиткой", "укладка керамогранита",
              "укладка мозаики", "затирка швов"],
        106: ["поклейка обоев", "оклейка обоями", "наклейка обоев", "клеим обои",
              "подбор рисунка обоев", "стыковка рисунка"],
        107: ["малярные работы", "покраска стен", "покраска потолка", "финишная отделка под покраску",
              "декоративная штукатурка", "венецианская штукатурка"],
        108: ["штукатурные работы", "механизированная штукатурка", "цементная штукатурка",
              "гипсовая штукатурка", "штукатурка по маякам", "выравнивание стен штукатуркой"],
        109: ["укладка ламината", "укладка паркета", "укладка линолеума", "стяжка пола",
              "выравнивание пола", "монтаж плинтусов", "укладка паркетной доски"],
        110: ["монтаж гипсокартона", "стены из гипсокартона", "потолок из гипсокартона",
              "перегородки из гипсокартона", "короб из гипсокартона", "ниши из гипсокартона"],
        111: ["демонтажные работы", "снос стен", "упаковка строительного мусора",
              "вывоз мусора", "демонтаж плитки", "демонтаж полов"],
    }

    # Проверка на точное совпадение с важными фразами
    if mc_id in exact_multiword_phrases and phrase in exact_multiword_phrases[mc_id]:
        return 1.0

    # Длинные фразы (3+ слова) имеют высокий приоритет
    if len(phrase.split()) >= 3:
        return 0.9

    # Фразы из 2 слов
    if len(phrase.split()) == 2:
        return 0.7

    # Короткие фразы - только если они специфичные
    specific_short = {
        102: ["бойлер", "водонагреватель", "полотенцесушитель", "канализация", "водоснабжение", "инсталляция"],
        103: ["штробление", "электромонтаж", "проводка", "электрощит", "автоматы"],
        104: ["натяжной", "натяжных", "пвх", "багет"],
        105: ["керамогранит", "мозаика", "кафель", "затирка"],
        106: ["стеклообои", "флизелиновые", "виниловые", "фотообои"],
        107: ["малярка", "короед", "венецианская", "шпатлевка", "шпаклевка"],
        108: ["ротбанд", "волма", "маяки", "короед", "барашек"],
        109: ["ковролин", "линолеум", "пробка", "кварцвинил", "наливной", "спс"],
        110: ["гкл", "гвл", "гипрок", "ud", "cd"],
        111: ["перфоратор", "отбойный"],
    }

    if mc_id in specific_short and phrase in specific_short[mc_id]:
        return 0.6

    return 0.3  # Базовый вес для остальных фраз


def detect_microcategories(description: str) -> Set[int]:
    """
    Определяет микрокатегории, представленные в тексте объявления.
    Использует поиск ключевых фраз с учетом контекста и весов.

    Args:
        description: Текст объявления

    Returns:
        Множество ID обнаруженных микрокатегорий
    """
    normalized = normalize_text(description)
    detected_ids = set()
    mc_scores = defaultdict(float)  # Накопленные баллы для каждой МК

    # Сначала проверяем приоритетные фразы для каждой категории
    priority_phrases = {
        101: ["ремонт под ключ", "комплексный ремонт", "полный ремонт", "комплекс работ", "генподряд"],
        102: ["сантехника", "сантехнические работы", "монтаж сантехники", "разводка труб", "опрессовка труб"],
        103: ["электрика", "электромонтажные работы", "замена проводки", "электрощит"],
        104: ["натяжные потолки", "натяжной потолок", "тканевые потолки", "пвх потолок"],
        105: ["укладка плитки", "плиточные работы", "облицовка плиткой", "укладка керамогранита"],
        106: ["поклейка обоев", "оклейка обоями", "клеим обои", "наклейка обоев"],
        107: ["малярные работы", "шпаклевка", "шпатлевка", "покраска стен", "покраска потолка"],
        108: ["штукатурка", "штукатурные работы", "механизированная штукатурка", "цементная штукатурка"],
        109: ["напольные покрытия", "укладка ламината", "укладка паркета", "стяжка пола", "укладка линолеума"],
        110: ["гипсокартон", "гкл", "монтаж гипсокартона", "перегородки из гипсокартона"],
        111: ["демонтаж", "демонтажные работы", "снос стен", "упаковка строительного мусора"],
    }

    # Проверяем приоритетные фразы
    for mc_id, phrases in priority_phrases.items():
        for phrase in phrases:
            if phrase in normalized:
                detected_ids.add(mc_id)
                mc_scores[mc_id] = max(mc_scores[mc_id], 1.0)
                break

    # Проверяем все фразы из словаря с весами
    for mc in MICROCATALOG:
        if mc.mcId in detected_ids:
            continue  # Уже найдена по приоритетным фразам

        for phrase in mc.keyPhrases:
            norm_phrase = normalize_text(phrase)

            # Пропускаем слишком короткие фразы
            if len(norm_phrase) < 3:
                continue

            # Проверяем наличие фразы в тексте
            if norm_phrase in normalized:
                score = calculate_phrase_score(norm_phrase, normalized, mc.mcId)
                mc_scores[mc.mcId] = max(mc_scores[mc.mcId], score)

                # Если分数 выше порога - добавляем категорию
                if score >= 0.6:
                    detected_ids.add(mc.mcId)
                    break

    # Дополнительный анализ для объявлений со структурированным форматом
    sentences = extract_sentences(description)

    # Анализ перечислений через "/"
    if "/" in description:
        parts = [normalize_text(p) for p in description.split("/") if p.strip()]
        for part in parts:
            for mc in MICROCATALOG:
                if mc.mcId in detected_ids:
                    continue
                for phrase in mc.keyPhrases[:15]:
                    if normalize_text(phrase) in part:
                        detected_ids.add(mc.mcId)
                        break

    # Анализ маркированных списков (строки с "-", "*", "•")
    if any(marker in description for marker in ["\n-", "\n*", "\n•", "- ", "* "]):
        lines = description.split("\n")
        for line in lines:
            line_normalized = normalize_text(line)
            if line_normalized.startswith("-") or line_normalized.startswith("*"):
                for mc in MICROCATALOG:
                    if mc.mcId in detected_ids:
                        continue
                    for phrase in mc.keyPhrases[:12]:
                        if normalize_text(phrase) in line_normalized:
                            detected_ids.add(mc.mcId)
                            break

    # Проверка для объявлений с явным перечислением услуг
    service_list_patterns = [
        r"(?:выполняем|делаем|предлагаем|оказываем).*?(?:также|а также|и)",
        r"(?:услуги|работы).*?(?:включая|такие как|например)",
    ]

    for pattern in service_list_patterns:
        matches = re.findall(pattern, description, re.IGNORECASE)
        if matches:
            for match in matches:
                match_normalized = normalize_text(match)
                for mc in MICROCATALOG:
                    if mc.mcId in detected_ids:
                        continue
                    for phrase in mc.keyPhrases[:10]:
                        if normalize_text(phrase) in match_normalized:
                            detected_ids.add(mc.mcId)
                            break

    return detected_ids